Cypress.Commands.add('getPasswordAdviceMatch', ()=>{
    let matchItems = 0
    cy.get('.mat-card-content > div > .mat-icon').each(($el, index, $list)=>
    {        
        if ($el.text() === 'done')
        {
            matchItems = matchItems + 1
        }        
     return matchItems
    })
})
